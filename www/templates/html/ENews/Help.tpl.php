            <div id="styleguide">
                <p><strong>Full sentences:</strong> Often people are tempted to enter an outline or bullet lists. Nebraska Today items are news briefs and we need full sentences. Calendar items are not required to be full sentences (although it would be just fine to use the same entry for both).</p>

                <p><strong>Dates:</strong> Time, Month, Date (in that order). No day of the week and no "th" or "st" at the end of the date.
                <br />
                Use "a.m." and "p.m." Abbreviate only: Jan., Feb., Aug., Sept., Oct., Nov., Dec. Use time ranges if they are pertinent
                Example: 2 p.m. May 19
                Example: 2-4 p.m. Dec. 12
                </p>

                <p><strong>Punctuation:</strong> Follow standard punctuation rules. Please: No exclamation points!!!!! No ampersands (&amp;) to shorten or replace the word "and". Ampersands only if they are officially in a brand name.</p>

                <p>Phone numbers have hyphens in them, not periods: 402-472-8515</p>

                <p><strong>Acronyms:</strong> Use an acronym, initials etc. only on second reference. Spell out the entire name in first reference. No need to put the acronym in parentheses after spelling it out on first reference</p>

                <p><strong>Capitals:</strong> Please no ALL CAPS.</p>

                <p><strong>Lectures:</strong> Give the title of the lecture, if possible, in quotes, following the name of the presenter<br />
                Example: Janet Jones, professor, University of California, Berkeley, will present "How to Increase Soybean Yields." Do not use italics</p>

                <p><strong>Professor/Dr./Ph.D.:</strong> Do not put "Dr." in front of a professor's name. "Professor" on first reference, is acceptable; so is putting Ph.D after a name. We prefer a department or discipline in relation to professor: "UNL sociology professor Max Smith"</p>

                <p><strong>Tone:</strong> These are news items not advertisements or promos. Third-person, not "you" or "we". People are not invited, they are welcome. Keep in mind this is an internal newsletter and it is OK to have fun and be light-hearted if possible but our academic world often frowns on it. Personal and professional are two words that come to mind in writing for an internal faculty/staff audience.</p>

                <p>Entice your reader to read more by writing an interesting, targeted summary. (300 characters max = 2 tweets)</p>

                <p>Headlines with dates in them reinforce the date of your event.</p>

                <p>If you have a deadline to submit something, purchase tickets or make a reservation, put it in the headline or the summary or the first paragraph of the full story. Don't make people read all the way to the end to find out they have a deadline.</p>

                <p><strong>Smart-quotes:</strong> Often quote marks and apostrophes do not translate well from word documents to the Web. Try to remember to turn off smart or "curly" quotes before copying and pasting something onto the Web.</p>
